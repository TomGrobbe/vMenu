    public class ValidWeapons
    {
        private ValidWeapon[] _validWeaponsList = new ValidWeapon[82 - 4];

        public ValidWeapon[] WeaponList
        {
            get
            {
                return _validWeaponsList ?? new ValidWeapon[0];
            }
        }

        public ValidWeapons()
        {
            var num = 0;

            foreach (var weapon in Weapons)
            {
                string realName = weapon.Key;
                string localizedName = weaponNames[realName];
                if (localizedName != "Invalid" && localizedName != "Night Vision" && localizedName != "Unarmed" && localizedName != "" && localizedName != "Parachute")
                {
                    uint hash = weapon.Value;
                    Dictionary<string, uint> componentHashes = new Dictionary<string, uint>();
                    foreach (var comp in weaponComponents)
                    {
                        if (CitizenFX.Core.Native.API.DoesWeaponTakeWeaponComponent(hash, comp.Value))
                        {
                            if (!componentHashes.ContainsKey(weaponComponentNames[comp.Key]))
                            {
                                componentHashes.Add(weaponComponentNames[comp.Key], comp.Value);
                            }
                        }
                    }
                    ValidWeapon vw = new ValidWeapon()
                    {
                        Hash = hash,
                        Name = localizedName,
                        Components = componentHashes,
                        Perm = weaponPermissions[realName]
                    };
                    _validWeaponsList[num] = vw;
                    num++;
                }
            }
        }

